package com.example.pti.model;

public class AlerteMode {

    private int verticality;
    private int immobility;

    public AlerteMode() {
    }

    public int getVerticality() {
        return verticality;
    }

    public void setVerticality(int verticality) {
        this.verticality = verticality;
    }

    public int getImmobility() {
        return immobility;
    }

    public void setImmobility(int immobility) {
        this.immobility = immobility;
    }
}
